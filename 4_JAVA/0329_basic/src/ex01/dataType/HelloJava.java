package ex01.dataType;
public class HelloJava {
	public static void main(String[] args) { // 시작점(진입점)
		System.out.println("12ssd1212");
		System.out.printf("%d %f %c %s", 100, 12.34, 'A', "STRING"); // 정형화 된 자릿수로 표현
		System.out.printf("%f \n", 12.34566); // 소수점 6자리
		System.out.printf("%1.3f \n", 12.34566);
	}
}
